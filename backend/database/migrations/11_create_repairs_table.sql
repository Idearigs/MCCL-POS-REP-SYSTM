-- Create repairs table for repair management
CREATE TABLE IF NOT EXISTS `repairs` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `repair_number` VARCHAR(50) NOT NULL,
  `customer_id` INT NOT NULL,
  `device_type` VARCHAR(100) NOT NULL,
  `brand` VARCHAR(100) NULL,
  `model` VARCHAR(100) NULL,
  `serial_number` VARCHAR(100) NULL,
  `problem_description` TEXT NOT NULL,
  `diagnosis` TEXT NULL,
  `repair_status` ENUM('received', 'diagnosed', 'waiting_parts', 'in_progress', 'completed', 'delivered', 'cancelled') NOT NULL DEFAULT 'received',
  `estimated_cost` DECIMAL(10,2) NULL,
  `final_cost` DECIMAL(10,2) NULL,
  `received_date` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `estimated_completion_date` DATE NULL,
  `completed_date` DATETIME NULL,
  `delivered_date` DATETIME NULL,
  `warranty_end_date` DATE NULL,
  `notes` TEXT NULL,
  `created_by` INT NULL,
  `technician_id` INT NULL,
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE INDEX `idx_repair_number` (`repair_number`),
  INDEX `idx_repair_customer` (`customer_id`),
  INDEX `idx_repair_status` (`repair_status`),
  INDEX `idx_repair_received_date` (`received_date`),
  INDEX `idx_repair_technician` (`technician_id`),
  CONSTRAINT `fk_repair_customer` FOREIGN KEY (`customer_id`) REFERENCES `customers` (`id`) ON DELETE RESTRICT,
  CONSTRAINT `fk_repair_created_by` FOREIGN KEY (`created_by`) REFERENCES `users` (`id`) ON DELETE SET NULL,
  CONSTRAINT `fk_repair_technician` FOREIGN KEY (`technician_id`) REFERENCES `users` (`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
